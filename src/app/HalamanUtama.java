    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int baris = tabel.rowAtPoint(evt.getPoint());
        String nik = tabel.getValueAt(baris, 1).toString();
        txt_nik.setText(nik);
        
        String nama = tabel.getValueAt(baris, 2).toString();
        txt_nama.setText(nama);
        
        String loker = tabel.getValueAt(baris, 3).toString();
        cmb_loker.setSelectedItem(loker);
        
        String barang = tabel.getValueAt(baris, 4).toString();
        txt_barang.setText(barang);
        
        String deskripsi = tabel.getValueAt(baris, 5).toString();
        txt_barang.setText(deskripsi);
        
        String tanggal = tabel.getValueAt(baris, 6).toString();
        txt_barang.setText(tanggal);
        

    }//GEN-LAST:event_tabelMouseClicked

