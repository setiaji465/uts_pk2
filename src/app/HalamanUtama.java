    private void tampilkan() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("no .");
        model.addColumn("kode dokumen");
        model.addColumn("nama dokumen");
        model.addColumn("kategori dokumen");
        model.addColumn("lokasi dokumen");
        model.addColumn("deksripsi dokumen");
         model.addColumn("tanggal");
        
        String pencarian= txt_cari.getText();
        try {
            int no = 1;
            String sql = "SELECT *FROM tb_penitip WHERE nama LIKE '%"+pencarian+"%' ";
            java.sql.Connection c = (Connection)Koneksi.sambungDB();
            java.sql.Statement s = c.createStatement();
            java.sql.ResultSet r = s.executeQuery(sql);
            
            while (r.next()) {
                model.addRow(new Object[]{no++,r.getString(1),
                        r.getString(2),r.getString(3),r.getString(4),r.getString(5),r.getString(6)});
            }
            tabel.setModel(model);
        } catch (Exception e) {
            System.out.println("Error : "+e.getMessage());
        }
    }
